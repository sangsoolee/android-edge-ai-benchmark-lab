#!/usr/bin/env python3
"""
compare_accuracy.py
-------------------
Compares FP32 vs INT8 TFLite accuracy results and generates a summary report.

Usage:
  python scripts/eval/compare_accuracy.py \\
    --fp32 results/accuracy/mobilenet_v3_small_fp32_results.json \\
    --int8 results/accuracy/mobilenet_v3_small_int8_results.json

Output:
  results/accuracy/summary.csv
  results/accuracy/summary.md   (README.md in that dir is the curated, hand-written analysis)

summary.csv columns:
  model, precision, top1_pct, top5_pct, top1_correct, top5_correct,
  sample_count, elapsed_sec, top1_drop_pct_point, top5_drop_pct_point
"""

import argparse
import csv
import json
from pathlib import Path


# ---------------------------------------------------------------------------
# Core
# ---------------------------------------------------------------------------

def load_result(path: Path) -> dict:
    with open(path) as f:
        data = json.load(f)
    # Normalise: accept both fraction (0-1) and percent (0-100) for accuracy fields
    for key in ("top1_accuracy", "top5_accuracy"):
        val = data.get(key, 0.0)
        if val <= 1.0:
            data[f"{key}_pct"] = round(val * 100, 3)
        else:
            data[f"{key}_pct"] = round(val, 3)
    return data


def compute_drops(fp32: dict, int8: dict) -> tuple[float, float]:
    top1_drop = round(fp32["top1_accuracy_pct"] - int8["top1_accuracy_pct"], 3)
    top5_drop = round(fp32["top5_accuracy_pct"] - int8["top5_accuracy_pct"], 3)
    return top1_drop, top5_drop


def write_summary_csv(fp32: dict, int8: dict, top1_drop: float, top5_drop: float,
                      output_dir: Path) -> Path:
    out = output_dir / "summary.csv"
    rows = []
    for r, t1d, t5d in [(fp32, 0.0, 0.0), (int8, top1_drop, top5_drop)]:
        rows.append({
            "model":               r["model"],
            "precision":           r["precision"],
            "top1_pct":            r["top1_accuracy_pct"],
            "top5_pct":            r["top5_accuracy_pct"],
            "top1_correct":        r.get("top1_correct", ""),
            "top5_correct":        r.get("top5_correct", ""),
            "sample_count":        r.get("sample_count", ""),
            "elapsed_sec":         r.get("elapsed_sec", ""),
            "top1_drop_pct_point": t1d if t1d != 0.0 else "",
            "top5_drop_pct_point": t5d if t5d != 0.0 else "",
        })
    fieldnames = list(rows[0].keys())
    with open(out, "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)
    return out


def write_summary_md(fp32: dict, int8: dict, top1_drop: float, top5_drop: float,
                     output_dir: Path) -> Path:
    """Auto-generated machine summary. Written to summary.md so it never clobbers
    the curated, hand-written README.md chapter in this directory."""
    n = fp32.get("sample_count", "?")
    sign_t1 = "-" if top1_drop >= 0 else "+"
    sign_t5 = "-" if top5_drop >= 0 else "+"
    model_base = fp32["model"].replace("_fp32.tflite", "").replace("_int8.tflite", "")

    # Drop-aware verdict — do NOT hard-code "minimal accuracy loss".
    if top1_drop < 1.0:
        verdict = f"Top-1 drop {top1_drop:.2f} pp — acceptable for INT8 deployment."
    elif top1_drop < 2.0:
        verdict = f"Top-1 drop {top1_drop:.2f} pp — review calibration quality."
    else:
        verdict = (f"Top-1 drop {top1_drop:.2f} pp — **accuracy collapse**; this INT8 "
                   "model is not deployable as-is (see README.md).")

    content = f"""\
# INT8 Accuracy Summary — {model_base} (auto-generated)

ImageNet validation, n={n:,}. See `README.md` in this directory for the full analysis.

| Precision | Model | Top-1 | Top-5 | Samples |
|---|---|---:|---:|---:|
| FP32 | `{fp32['model']}` | {fp32['top1_accuracy_pct']:.2f}% | {fp32['top5_accuracy_pct']:.2f}% | {n:,} |
| INT8 | `{int8['model']}` | {int8['top1_accuracy_pct']:.2f}% | {int8['top5_accuracy_pct']:.2f}% | {int8.get('sample_count', n):,} |
| Drop | | {sign_t1}{abs(top1_drop):.2f} pp | {sign_t5}{abs(top5_drop):.2f} pp | |

**Verdict:** {verdict}

*Generated by `scripts/eval/compare_accuracy.py`.*
"""
    out = output_dir / "summary.md"
    out.write_text(content)
    return out


def print_report(fp32: dict, int8: dict, top1_drop: float, top5_drop: float) -> None:
    print(f"\n{'='*56}")
    print(f"  {'':30s} {'Top-1':>8}  {'Top-5':>8}")
    print(f"  {'-'*54}")
    print(f"  {'FP32 (' + fp32['model'] + ')':<30s} {fp32['top1_accuracy_pct']:>7.2f}%  {fp32['top5_accuracy_pct']:>7.2f}%")
    print(f"  {'INT8 (' + int8['model'] + ')':<30s} {int8['top1_accuracy_pct']:>7.2f}%  {int8['top5_accuracy_pct']:>7.2f}%")
    print(f"  {'-'*54}")
    sign1 = "-" if top1_drop >= 0 else "+"
    sign5 = "-" if top5_drop >= 0 else "+"
    print(f"  {'Drop (FP32 → INT8)':<30s} {sign1}{abs(top1_drop):>6.2f} pp  {sign5}{abs(top5_drop):>6.2f} pp")
    print(f"{'='*56}\n")

    samples = fp32.get("sample_count", "?")
    print(f"  Samples evaluated : {samples:,}" if isinstance(samples, int) else f"  Samples: {samples}")

    if isinstance(top1_drop, float) and abs(top1_drop) < 1.0:
        print("  ✅ Top-1 drop < 1 pp — acceptable for production INT8 deployment")
    elif isinstance(top1_drop, float) and abs(top1_drop) < 2.0:
        print("  ⚠️  Top-1 drop 1-2 pp — review calibration quality")
    else:
        print("  ❌ Top-1 drop > 2 pp — re-calibrate with more/better representative data")


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> None:
    repo_root = Path(__file__).parents[2]
    default_out = repo_root / "results" / "accuracy"

    parser = argparse.ArgumentParser(
        description="Compare FP32 vs INT8 TFLite accuracy results"
    )
    parser.add_argument("--fp32", required=True, type=Path,
                        help="FP32 results JSON from accuracy_eval.py")
    parser.add_argument("--int8", required=True, type=Path,
                        help="INT8 results JSON from accuracy_eval.py")
    parser.add_argument("--output-dir", type=Path, default=default_out,
                        help=f"Output directory (default: {default_out})")
    args = parser.parse_args()

    for p in (args.fp32, args.int8):
        if not p.exists():
            raise SystemExit(f"File not found: {p}")

    fp32 = load_result(args.fp32)
    int8 = load_result(args.int8)

    top1_drop, top5_drop = compute_drops(fp32, int8)

    print_report(fp32, int8, top1_drop, top5_drop)

    args.output_dir.mkdir(parents=True, exist_ok=True)
    csv_path = write_summary_csv(fp32, int8, top1_drop, top5_drop, args.output_dir)
    md_path  = write_summary_md(fp32, int8, top1_drop, top5_drop, args.output_dir)

    print(f"  → {csv_path}")
    print(f"  → {md_path}")
    print(f"  (curated analysis: {args.output_dir / 'README.md'})\n")


if __name__ == "__main__":
    main()
